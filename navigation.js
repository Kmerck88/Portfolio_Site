// Function to simulate click on the link
function navigateToSection(sectionId) {
    document.getElementById(sectionId).click();
}

// Example usage
navigateToSection('aboutLink'); // Navigate to "Meet ME" section
navigateToSection('projectsLink'); // Navigate to "Projects" section
navigateToSection('contactLink'); // Navigate to "Contact" section
navigateToSection('resumeLink'); // Download Resume
